// Função para obter conteúdo por ID
export const getContentById = (type, id) => {
  const contentMap = {
    breathing: breathingExercises,
    meditation: guidedMeditations,
    tips: wellnessTips,
    info: mentalHealthInfo
  }
  
  const content = contentMap[type]
  return content ? content.find(item => item.id === id) : null
}

